package com.operators;

public class RelationalOperatorDemo {

	public static void main(String[] args) {
	int a = 5;
	int b = 25;
	
	System.out.println("The result of a==b is "+(a==b));
	System.out.println("The result of a!=b is "+(a!=b));
	System.out.println("The result of a<b  is "+(a<b));
	System.out.println("The result of a>b  is "+(a>b));
	System.out.println("The result of a<=b is "+(a<=b));
	System.out.println("The result of a>=b is "+(a>=b));

	}

}
